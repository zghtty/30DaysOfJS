let age = 35;        // 年龄设为35
const gravity = 9.81; // 我们用const来表示不变的值，这里是重力常数，单位为米每二次方秒
let mass = 72;        // 质量，单位为千克
const PI = 3.14;      // π，一个几何常数

// 更多例子
const boilingPoint = 100; // 温度，单位为摄氏度，水的沸点，是一个常量
const bodyTemp = 37;      // 摄氏度，人类平均体温，是一个常量

console.log(age, gravity, mass, PI, boilingPoint, bodyTemp);